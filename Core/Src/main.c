/* USER CODE BEGIN PTD */
#define PID_MAX 800
#define PID_MIN 0
double INTEGRAL = 0, pre_err = 0;
const double Kp = 0, Ki = 0, Kd = 0;
double goal = 0;

double PID(double in, double goal) //位移式PID
{
  double err = goal - in;
  INTEGRAL += err * 20;
  double derr = (err - pre_err) / 20;
  pre_err = err;
  double pid = (Kp * err) + (Ki * INTEGRAL) + (Kd * derr);
  double output;
  if (pid < PID_MIN)
    output = PID_MIN;
  else if (pid > PID_MAX)
    output = PID_MAX;
  else
    output = pid;
  return output;
}
